/**
 * *_strncpy - It copies string.
 * @dest: The destination where the string will be copyied.
 * @src: The string's source.
 * @n: The amount of characters that will be copied.

 *Return: The connected strings.
 */
char *_strncpy(char *dest, char *src, int n)
{
	int i, j;
	char *s = dest;

	i = 0;
	while (src[i] != '\0' && i < n - 1)
	{
		dest[i] = src[i];
		i++;
	}
	if (i < n)
	{
		j = i;
		while (j < n)
		{
			dest[j] = '\0';
			j++;
		}
	}
	return (s);
}

