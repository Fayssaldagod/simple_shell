/**
 * _realloc - Adjusts the memory block's size.
 * @ptr: The pointer to the previous allocated block.
 * @old_size: size in bytes of the previous block.
 * @new_size: size of bytes of the new block.
 *
 * Return: The pointer to the blocks.
 */
void *_realloc(void *ptr, unsigned int old_size, unsigned int new_size)
{
	char *p;

	if (!ptr)
		return (malloc(new_size));
	if (!new_size)
		return (free(ptr), NULL);
	if (new_size == old_size)
		return (ptr);

	p = malloc(new_size);
	if (!p)
		return (NULL);

	old_size = old_size < new_size ? old_size : new_size;
	while (old_size--)
		p[old_size] = ((char *)ptr)[old_size];
	free(ptr);
	return (p);
}
